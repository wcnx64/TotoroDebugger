// reference: https://github.com/kokke/tiny-AES-C
// The round constant word array, Rcon[i], contains the values given by 
// x to the power (i-1) being powers of x (x is denoted as {02}) in the field GF(2^8)
static const unsigned char g_standard_rcon[11] = {
  0x8d, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36 };

// empty key, default sbox, default reverse sbox
p_ha_ctx ha_build_aes_ctx(int bits) {
	p_ha_ctx ctx = NULL;
	if (bits == 0) {
		// default to 256
		bits = 256;
	}
	if (bits == 256 || bits == 192 || bits == 128) { // verify bits
		// allocate and build
		ctx = (p_ha_ctx)malloc(sizeof(ha_ctx));
		if (ctx) {
			memset(ctx, 0, sizeof(*ctx));
			ctx->bits = bits;
			memcpy(ctx->sbox, g_standard_sbox, sizeof(ctx->sbox));
			memcpy(ctx->rsbox, g_standard_rsbox, sizeof(ctx->rsbox));
			memcpy(ctx->rcon, g_standard_rcon, sizeof(ctx->rcon));
		}
	}
	return ctx;
}

void ha_destroy_aes_ctx(p_ha_ctx ctx) {
	if (ctx) {
		// though it is a hack tool,
		// it still follows sensitive data management standard
		memset(ctx, 0, sizeof(*ctx));
		free(ctx);
	}
}

void ha_set_key(p_ha_ctx ctx, unsigned char* key) {
	memcpy(ctx->key, key, sizeof(ctx->key));
}

void ha_set_sbox(p_ha_ctx ctx, unsigned char* sbox) {
	memcpy(ctx->sbox, sbox, sizeof(ctx->sbox));
}

void ha_set_rcon(p_ha_ctx ctx, unsigned char* rcon) {
	memcpy(ctx->rcon, rcon, sizeof(ctx->rcon));
}

// calculate reverse sbox
unsigned char* ha_calculate_rsbox(p_ha_ctx ctx) {
	char hit[sizeof(ctx->sbox)] = { 0 }; // for validation
	// calculate reverse rbox
	for (int i = 0; i < sizeof(ctx->sbox); i++) {
		ctx->rsbox[ctx->sbox[i]] = i;
		hit[ctx->sbox[i]] = 1;
	}
	// validate the result
	for (int i = 0; i < sizeof(hit); i++) {
		if (hit[i] == 0) { // incomplete reverse
			// sbox and rsbox are both invalid
			// only the rsbox is cleared
			memset(ctx->rsbox, 0, sizeof(ctx->rsbox));
			return NULL;
		}
	}
	return ctx->rsbox;
}

// get param by bits
static void ha_get_aes_params_by_bits(
	int bits,
	int* key_len,
	int* roundkey_size) {
	// private function, prefer performance, no param validation
	if (bits == 128) {
		*key_len = 16;
		*roundkey_size = 176;
	}
	else if (bits == 192) {
		*key_len = 24;
		*roundkey_size = 208;
	}
	else { // bits == 256
		*key_len = 32;
		*roundkey_size = 240;
	}
}

// offset: round offset which should be aligned by 16bits.
// length: known round key length which should be greater than or equal to key length
void ha_set_roundkey(p_ha_ctx ctx, unsigned char* roundkey, int offset, int length) {
	// get params by bits
	int key_len = 0;
	int roundkey_size = 0;
	ha_get_aes_params_by_bits(ctx->bits, &key_len, &roundkey_size);
	// check alignment
	if (offset % 16 != 0)
		return;
	// check data sufficiency
	if (length < key_len)
		return;
	// check overflow
	if (offset + length > roundkey_size)
		return;
	// set known partial round key
	ctx->roundkey_offset = offset;
	ctx->roundkey_length = (length > key_len) ? key_len : length;
	memcpy(ctx->roundkey + offset, roundkey, length);
}

// calculate the given partial roundkey's corresponding key
unsigned char* ha_calulate_key(p_ha_ctx ctx) {
	unsigned char* key = ctx->key;
	unsigned char* rk = ctx->roundkey;
	unsigned char* sbox = ctx->sbox;
	unsigned char* rcon = ctx->rcon;
	unsigned char  temp[4] = { 0 };
	int            i = 0, j = 0;
	for (i = 0x9; i >= 0x0; i--) {
		for (j = 0x10 * i + 0xb; j >= 0x10 * i + 0x0; j--)
			rk[j + 0x4] = rk[j + 0x10 + 0x4] ^ rk[j + 0x10];
		temp[0x0] = rk[0x10 * i + 0x0d];
		temp[0x1] = rk[0x10 * i + 0x0e];
		temp[0x2] = rk[0x10 * i + 0x0f];
		temp[0x3] = rk[0x10 * i + 0x0c];
		temp[0x0] = sbox[temp[0x0]];
		temp[0x1] = sbox[temp[0x1]];
		temp[0x2] = sbox[temp[0x2]];
		temp[0x3] = sbox[temp[0x3]];
		temp[0x0] = temp[0x0] ^ rcon[i + 1];
		rk[0x10 * i + 0x0] = rk[0x10 * i + 0x10] ^ temp[0x00];
		rk[0x10 * i + 0x1] = rk[0x10 * i + 0x11] ^ temp[0x01];
		rk[0x10 * i + 0x2] = rk[0x10 * i + 0x12] ^ temp[0x02];
		rk[0x10 * i + 0x3] = rk[0x10 * i + 0x13] ^ temp[0x03];
	}
	for (i = 0x0; i < 0xf; i++)
		key[i] = rk[i];
	return key;
}

// AES 128
// calculate the full roundkey by key
// build the first block
// then do block cipher, block by block as the following, to other blocks
// substitute with sbox
// the 1st item of the substituted 4 items xor with rcon
// roundkey xor with substituted values
// shift
unsigned char* ha_calulate_full_roundkey_128(p_ha_ctx ctx) {
	unsigned char* key = ctx->key;
	unsigned char* rk = ctx->roundkey;
	unsigned char* sbox = ctx->sbox;
	unsigned char* rcon = ctx->rcon;
	unsigned char  temp[4] = { 0 };
	int            i = 0, j = 0;
	for (i = 0x0; i < 0xf; i++)
		rk[i] = key[i];
	for (i = 0x0; i < 0xa; i++) {
		temp[0x0] = rk[0x10 * i + 0x0d];
		temp[0x1] = rk[0x10 * i + 0x0e];
		temp[0x2] = rk[0x10 * i + 0x0f];
		temp[0x3] = rk[0x10 * i + 0x0c];
		temp[0x0] = sbox[temp[0x0]];
		temp[0x1] = sbox[temp[0x1]];
		temp[0x2] = sbox[temp[0x2]];
		temp[0x3] = sbox[temp[0x3]];
		temp[0x0] = temp[0x0] ^ rcon[i + 1];
		rk[0x10 * i + 0x10] = rk[0x10 * i + 0x0] ^ temp[0x00];
		rk[0x10 * i + 0x11] = rk[0x10 * i + 0x1] ^ temp[0x01];
		rk[0x10 * i + 0x12] = rk[0x10 * i + 0x2] ^ temp[0x02];
		rk[0x10 * i + 0x13] = rk[0x10 * i + 0x3] ^ temp[0x03];
		for (j = 0x10 * i + 0x0; j < 0x10 * i + 0xc; j++)
			rk[j + 0x10 + 0x4] = rk[j + 0x4] ^ rk[j + 0x10];
	}
	return rk;
}

